# хідні данні
A = int(input("Введіть двозначне число: "))

# Перевірка, на двозначність
if 10 <= A <= 99:
    # Знаходження десятків
    desyatki = A // 10

    # Знаходження одиниць
    odinici = A % 10

    # Виведення результату
    print("Десятки: ", desyatki)
    print("Одиниці: ", odinici)
else:
    print("Помилка: введене число не є двозначним!")
